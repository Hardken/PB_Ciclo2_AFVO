    private void btn_confirmar_pedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_confirmar_pedidoActionPerformed
        // TODO add your handling code here:
        int cant_producto = Integer.parseInt(txt_cant_producto.getText());
        String user_name = txt_username.getText();
        String type_request = list_type_pedido.getSelectedItem().toString();
        int balance_account = 0;
        int id_producto = Integer.parseInt(txt_product_id.getText());
        int total = 0;
        boolean find_pet = false;
        if (type_request.equals("Pedido Simple") || cant_producto <= 20) {
            JOptionPane.showMessageDialog(this, "Un pedido simple solo puede realizar una compra max de 20 unidades");
            for (accountClass account : accountObjectList) {
                if (user_name.equals(account.getUser_name())) {
                    //consulta valor existente
                    balance_account = account.getAccount_balance();
                    for (productClass producto : productObjectList) {
                        if (id_producto == producto.getProduct_id()) {
                            total = cant_producto * producto.getCost();
                            if (balance_account < total) {
                                JOptionPane.showMessageDialog(this, "No posee fondos suficientes para realizar la compra");
                            } else {
                                txt_costo.setText("El costo total es de: " + total);
                                int new_balance = balance_account - total;
                                account.setAccount_balance(new_balance);
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "no se encontro un producto registrado con este id");
                        }
                    }

                    break;
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontro ninguna cuenta con el nombre de usuario suministrado");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "El pedido posee una cantidad mayor a las 20 unidades, se actualizara a pedido compuesto");
            for (accountClass account : accountObjectList) {
                if (user_name.equals(account.getUser_name())) {
                    //consulta valor existente
                    balance_account = account.getAccount_balance();
                    for (productClass producto : productObjectList) {
                        if (id_producto == producto.getProduct_id()) {
                            total = cant_producto * producto.getCost();
                            if (balance_account < total) {
                                JOptionPane.showMessageDialog(this, "No posee fondos suficientes para realizar la compra");
                            } else {
                                txt_costo.setText("El costo total es de: " + total);
                                int new_balance = balance_account - total;
                                account.setAccount_balance(new_balance);
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "no se encontro un producto registrado con este id");
                        }
                    }

                    break;
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontro ninguna cuenta con el nombre de usuario suministrado");
                }
            }
        }
    }//GEN-LAST:event_btn_confirmar_pedidoActionPerformed
    public void show_products() {
        DefaultListModel products_list = new DefaultListModel();
        int index_list = 0;
        for (productClass product : productObjectList) {
            String registro_products = product.getName() + " - " + "$" + product.getCost() + " - " + product.getStock() + " unds";
            products_list.add(index_list, registro_products);
            index_list++;
        }
        product_list.setModel(products_list);
    }

    public void show_acconunt() {
        DefaultListModel account_list = new DefaultListModel();
        int index_list = 0;
        for (accountClass account : accountObjectList) {
            String registro_accounts = account.getUser_name() + " - " + account.getName() + " - " + account.getEmail() + " - " + account.getPhone() + "-" + account.getAddress() + " - " + account.getCard_number() + " - " + account.getAccount_balance();
            account_list.add(index_list, registro_accounts);
            index_list++;
        }
        accounts_list.setModel(account_list);
    }

