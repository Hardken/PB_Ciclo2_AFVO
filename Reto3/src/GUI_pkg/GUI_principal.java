    private void btn_add_productsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_productsActionPerformed
        // TODO add your handling code here:
        try {
            int product_id = Integer.parseInt(txt_product_id.getText());
            String product_name = txt_product_name.getText();
            int product_price = Integer.parseInt(txt_product_cost.getText());
            int product_stock = Integer.parseInt(txt_product_stock.getText());
            if (product_name.equals("") || product_price == 0 || product_stock == 0) {
                JOptionPane.showMessageDialog(this, "Complete todos lo campos para continuar");
            } else {
                productClass product = new productClass(product_id, product_name, product_stock, product_price);
                productObjectList.add(product);
                clearfieldsproducts();
                show_products();
                JOptionPane.showMessageDialog(this, "Producto agregado con exito");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar producto");
        }
    }//GEN-LAST:event_btn_add_productsActionPerformed

